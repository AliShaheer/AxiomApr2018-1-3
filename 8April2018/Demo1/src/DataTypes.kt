fun main(args: Array<String>) {
    var intType: Int = 10
    println(intType)

    var myDouble: Double = 20.9
    println(myDouble)

    var myLong: Long = 2223
    println(myLong)

    var myFloat: Float = 33.0f
    println(myFloat)

    var myShort: Short = 12
    println(myShort)

    var mybyte: Byte = 8
    println(mybyte)

    var myChar: Char = 'A'
    println(myChar)

    var myString: String = "Hello World"
    println(myString)


}